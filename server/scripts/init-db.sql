-- Create camera_bodies table
CREATE TABLE IF NOT EXISTS camera_bodies (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    brand VARCHAR(100) NOT NULL,
    model VARCHAR(255) NOT NULL,
    body_type VARCHAR(100) NOT NULL,
    sensor_type VARCHAR(100) NOT NULL,
    mount_type VARCHAR(50) NOT NULL,
    resolution_mp DECIMAL(6,1) NOT NULL,
    sensor_width_mm DECIMAL(5,2) NOT NULL,
    sensor_height_mm DECIMAL(5,2) NOT NULL,
    max_iso INTEGER,
    min_iso INTEGER,
    has_ibis BOOLEAN DEFAULT false,
    continuous_shooting_fps DECIMAL(4,1),
    max_shutter_speed VARCHAR(20),
    min_shutter_speed VARCHAR(20),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    UNIQUE(brand, model)
);

-- Create indexes for camera_bodies
CREATE INDEX IF NOT EXISTS idx_camera_bodies_brand ON camera_bodies(brand);
CREATE INDEX IF NOT EXISTS idx_camera_bodies_mount_type ON camera_bodies(mount_type);
CREATE INDEX IF NOT EXISTS idx_camera_bodies_body_type ON camera_bodies(body_type);

-- Create lenses table
CREATE TABLE IF NOT EXISTS lenses (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    brand VARCHAR(100) NOT NULL,
    model VARCHAR(255) NOT NULL,
    mount_type VARCHAR(50) NOT NULL,
    min_focal_length INTEGER NOT NULL,
    max_focal_length INTEGER NOT NULL,
    min_aperture DECIMAL(3,1) NOT NULL,
    max_aperture DECIMAL(3,1) NOT NULL,
    filter_thread_size INTEGER,
    min_focus_distance_m DECIMAL(5,2),
    is_zoom BOOLEAN GENERATED ALWAYS AS (min_focal_length != max_focal_length) STORED,
    has_stabilization BOOLEAN DEFAULT false,
    weather_sealed BOOLEAN DEFAULT false,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    UNIQUE(brand, model)
);

-- Create indexes for lenses
CREATE INDEX IF NOT EXISTS idx_lenses_brand ON lenses(brand);
CREATE INDEX IF NOT EXISTS idx_lenses_mount_type ON lenses(mount_type);
CREATE INDEX IF NOT EXISTS idx_lenses_focal_range ON lenses(min_focal_length, max_focal_length);
CREATE INDEX IF NOT EXISTS idx_lenses_aperture_range ON lenses(min_aperture, max_aperture);

